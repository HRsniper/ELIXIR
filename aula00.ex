# Quando você instala o Elixir, você terá três novos executáveis: iex, elixire elixirc.

# Executando scripts
  # Depois de se familiarizar com o básico da linguagem, você pode tentar escrever programas simples. Isso pode ser feito colocando o seguinte código Elixir em um arquivo:

IO.puts "Hello world from Elixir"

# Salve-o como simple.exse execute-o com elixir:

# elixir simple.exs

# Quando se trata de Elixir, você notará que há duas extensões de arquivo, .ex e .exs
# Cada extensão tem seu próprio propósito:
  # .ex é usado para compilação, enquanto
  # .exs é usado para scripts e testes.
